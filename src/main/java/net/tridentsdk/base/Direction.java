package net.tridentsdk.base;

/**
 * Represents a direction
 *
 * @author TridentSDK
 * @since 0.5-alpha
 */
public enum Direction {

    /**
     * Downwards
     */
    DOWN,

    /**
     * Upwards
     */
    UP,

    /**
     * North
     */
    NORTH,

    /**
     * South
     */
    SOUTH,

    /**
     * West
     */
    WEST,

    /**
     * East
     */
    EAST

}
